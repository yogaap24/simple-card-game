// Helper: Check if a 3-char fragment is a complete valid word (to avoid in CARD GENERATION)
// Note: This is ONLY for filtering card generation, NOT for word validation
export const isCompleteWord = (fragment) => {
	if (fragment.length !== 3) return false;
	return BASE_WORDS.includes(fragment);
};

