// Helper: Check if a 2-character fragment is valid for table cards
// Rules: Must be 2 chars, must have vowel+consonant, cannot be "nd", "ng", or "aa"
export const isValidTableFragment = (fragment) => {
	if (fragment.length !== 2) return false;

	const vowels = 'aeiou';
	const consonants = 'bcdfghjklmnpqrstvwxyz';

	const char1 = fragment[0].toLowerCase();
	const char2 = fragment[1].toLowerCase();

	const char1IsVowel = vowels.includes(char1);
	const char1IsConsonant = consonants.includes(char1);
	const char2IsVowel = vowels.includes(char2);
	const char2IsConsonant = consonants.includes(char2);

	// Must have at least one vowel and one consonant
	if (!((char1IsVowel && char2IsConsonant) || (char1IsConsonant && char2IsVowel))) {
		return false;
	}

	return true;
};

